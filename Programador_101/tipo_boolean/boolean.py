#Os tipos de dados bool (boolean)
#Ao questionar algo em um programa,
#Só existem duas respostas possíveis:
#Sim (true) ou não (False)
#Existem vários operadores para "Quationar".
#Dentre eles o ==, que é o operador lógico que
#questiona se um valor  é igual a outro

#Para questionar se dois valores são iguais.
print(10 == 10)

#Para questionar se um valor é maior que outro
print(10 > 10)

#Para questionar se um valor é menor que outro
print(10 < 10)

#Para comparar limites
print(10 >= 11)
print(10 <= 11)